CREATE OR REPLACE FORCE VIEW SAJET.SYS_PROGRAM_FUN
(
   PROGRAM,
   FUNCTION,
   AUTH_SEQ,
   AUTHORITYS,
   DLL_FILENAME,
   FUN_IDX,
   FUN_PARAM,
   FUN_TW,
   SHOW_FLAG,
   FUN_TYPE,
   FUN_TYPE_IDX,
   FUN_TYPE_TW,
   FUN_TYPE_CN,
   WEB_FLAG,
   ENABLED,
   FORM_NAME,
   FUN_TYPE_ENG,
   FUN_ENG,
   FUN_DESC_ENG,
   FUN_DESC_TW,
   FUN_DESC_CN,
   FUN_CN,
FUN_TYPE_VN,
FUN_VN,
FUN_DESC_VN
)
AS
   SELECT b.program,
          b.FUNCTION,
          c.auth_seq,
          c.authoritys,
          b.dll_filename,
          b.fun_idx,
          b.fun_param,
          b.fun_tw,
          b.show_flag,
          b.fun_type,
          b.fun_type_idx,
          b.fun_type_tw,
          b.fun_type_cn,
          b.web_flag,
          b.enabled,
          b.form_name,
          b.fun_type_eng,
          b.fun_eng,
          b.fun_desc_eng,
          b.fun_desc_tw,
          b.fun_desc_cn,
          b.fun_cn,
b.fun_type_VN,
b.fun_VN,
b.fun_desc_VN
     FROM sajet.sys_program_fun_name b, sajet.sys_program_fun_authority c
    WHERE b.program = c.program(+) AND b.FUNCTION = c.FUNCTION(+);
